QString RTF2HTML::quoteString(const QString &_str, quoteMode mode)
{
    QString str = _str;
    str.replace('&',  "&amp;");
    str.replace('<',  "&lt;");
    str.replace('>',  "&gt;");
    str.replace('\"', "&quot;");
    str.remove('\r');
    switch (mode){
    case quoteHTML:
        str.replace('\n', "<br>\n");
        break;
    case quoteXML:
        str.replace('\n', "<br/>\n");
        break;
    default:
        break;
    }
    QRegExp re("  +");
    int len;
    int pos = 0;

	while ((pos = re.indexIn(str, pos)) != -1) {
        len = re.matchedLength();

        if (len == 1)
            continue;
        QString s = " ";
        for (int i = 1; i < len; i++)
            s += "&nbsp;";
        str.replace(pos, len, s);
    }
    return str;
}

